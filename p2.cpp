#include<iostream>
using namespace std;

int main()
{
	int a,b;	
	cout<<"\n enter the value of a = ";
	cin>>a;
	cout<<"\n enter the value of b = ";
	cin>>b;
	cout<<"\n"<<a<<" + "<<b<<" = "<<a+b;
	cout<<"\n"<<a<<" - "<<b<<" = "<<a-b;
	cout<<"\n"<<a<<" * "<<b<<" = "<<a*b;
	cout<<"\n"<<a<<" / "<<b<<" = "<<a/b;	
	return 0;
}
